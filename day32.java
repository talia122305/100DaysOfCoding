public class Day32 {
    public static void main(String[] args) {
        int a = 4;
        int b = 12;

        // Operator logika OR tanpa if
        boolean hasil = (a > 8) || (b > 10);

        System.out.println("Nilai a = " + a);
        System.out.println("Nilai b = " + b);
        System.out.println("Hasil dari (a > 8 atau b > 10) = " + hasil);
    }
}
