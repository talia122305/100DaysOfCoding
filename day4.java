public class Day4 {
    public static void main(String[] args) {
        System.out.println("Halo,\nDunia!");                     // \n = baris baru
        System.out.println("Nama:\tDesi Natalia");               // \t = tab
        System.out.println("Dia berkata: \"Halo, saya Desi!\""); // \" = tanda kutip
        System.out.println("Folder: C:\\Users\\Desi\\Dokumen");  // \\ = backslash
        System.out.println("Simbol hati: \u2665");               // Unicode simbol hati
    }
}
