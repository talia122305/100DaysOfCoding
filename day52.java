public class day52 {
    public static void main(String[] args) {
        int i = 1; // nilai awal
        System.out.println("=== Program Perulangan While ===");
        // perulangan while
        while (i <= 5) {
            System.out.println("Angka ke-" + i);
            i++; // menambah nilai i
        }
        System.out.println("Perulangan selesai!");
    }
}
