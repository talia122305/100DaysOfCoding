public class day8 {
    public static void main(String[] args) {
        String nama = "Budi";
        String salam = "Halo, " + nama + "!";

        System.out.println(salam);                         // Menampilkan salam
        System.out.println("Panjang nama: " + nama.length());   // Menghitung panjang string
        System.out.println("Huruf pertama: " + nama.charAt(0)); // Mengambil karakter pertama
        System.out.println("Huruf besar: " + nama.toUpperCase()); // Mengubah ke huruf besar
    }
}
